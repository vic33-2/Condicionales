dia = int(input("ingresa un numero del 1 al 7: "))
if dia == 1:
    print("lunes")
elif dia == 2:
    print("martes")
elif dia == 3:
    print("miercoles")
elif dia == 4:
    print("jueves")            
elif dia == 5:
    print("viernes")
elif dia == 6:
    print("sabado")
elif dia == 7:
    print("domingo")    

else:
    print("Error: numero incorecto.")    